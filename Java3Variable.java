public class Java3Variable{
    public static void main(String[]args){
        int num1= 12;// It's dynamic initialization
        boolean b=true;// It's dynamic initialization
        char c='w';// It's dynamic initialization
        byte number=120;// It's dynamic initialization
        short number2=2000;// It's dynamic initialization
        int number3= 23333333;// It's dynamic initialization
        long number4=999999999;// It's dynamic initialization
        float number5=90.9f; //Decimal
        double number6=129.90;

        System.out.println(num1);
        System.out.println(b);
        System.out.println("C="+b);
        System.out.println("D="+c);
        System.out.println(number);
        System.out.println("b="+number);
        System.out.println(number2);
        System.out.println("AB="+number2);
        System.out.println(number3);
        System.out.println("ABC="+number3);
        System.out.println(number4);
        System.out.println("ABCD="+number4);
        System.out.println(number5);
        System.out.println("ABCDE="+number5);
        System.out.println(number6);
        System.out.println("ABCDEF="+number6);
    }
}
